'''
True > Verdadeiro
False > Falso
None > Referencia nada
Conectivos > and, or, not
>, >= ,<, <=, ==, !=
'''
#True e False
print('True e False')
print(2 > 3)
print(2 < 3)
primeiro_1 = 2 > 3
print(primeiro_1)
variavel1 = True
print(variavel1)

#Conectivos
print('\n')
print('Conectivos')
teste = 2 < 3 and 1 > 4
teste2 = 2 < 3 or 1 > 4
print(teste)
print(teste2)
print(f'\n{not teste}')

