# Uma abstração é uma visão ou representação de uma entidade que inclui apenas os atributos
# mais significativos. Em um sentido geral, a abstração permite coletar instâncias de entidades
# em grupos nos quais seus atributos comuns não precisam ser considerados. Por exemplo, suponha
# que definimos pássaros como criaturas com os seguintes atributos: duas asas, duas pernas,
# uma cauda e penas. Sebesta - Concepts of programming languages 10th


'''
Abstração 
Na visão de sebesta, temos dois tipos importantes de abstração na programação
 - Abstração de processos
 - Abstração de dados
'''

'''
Classe:
    - Conjunto de pessoas que tem a mesma função, os mesmos interesses ou a mesma condição numa
      sociedade: a classe operária
    - Na programação uma classe pode ser uma "estrutura" para abstração de dados. Uma maneira de dizer
      a linguagem que você quer construir seu próprio tipo.
'''

'''
Atributos:
    - Adjetivos aquilo que é atribuído a algo
    - O pássaro preto -> Preto é um adjetivo referente a cor do pássaro
'''

'''
Métodos
    - Métodos são a abstração de processos de uma abstração de dados
    - Os números executam operações com outro números. Soma, subtração, divisão ...
'''

'''
Instância
    - Um anglicismo, ou seja, uma palavra aportuguesada de instance. Que nada mais que dizer "Exemplo"
    - Classe pássaros é uma abstração de pássaro, ela é genérica. já a instância é o pássaro
'''
